let tipo  = 1;

if(tipo == "1") {
    console.log("1 é igual a '1', em comparação com valor");
}

if(tipo === "1") {
    console.log("1 é igual a '1', e o tipo é igual"); // correto a se usar no dia a dia
}

if(tipo != "1") {
    console.log("o valor é diferente de 1");
}

if(tipo !== "1") {
    console.log("o valor e o tipo são diferentes de 1");
}